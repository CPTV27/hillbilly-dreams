#!/usr/bin/env tsx
/**
 * Multi-LLM evaluation panel — submits the same prompt to multiple LLMs in
 * parallel and stores their responses for side-by-side review and synthesis.
 *
 * The pattern Chase used manually for the Studio C workflow stress test (Claude +
 * Gemini-class + GPT-class), automated.
 *
 * Usage:
 *   npx tsx scripts/multi-llm-eval.ts --target <target> --type <eval-type> [--models <comma-list>] [--synthesize]
 *
 * Examples:
 *   # Audit a public marketing page across 4 LLMs:
 *   npx tsx scripts/multi-llm-eval.ts \\
 *     --target https://bigmuddytouring.com/the-case \\
 *     --type page-audit
 *
 *   # Stress-test a strategy doc and ask Claude to synthesize:
 *   npx tsx scripts/multi-llm-eval.ts \\
 *     --target docs/positioning/mbt-product-definition-2026-04-30.md \\
 *     --type strategy-stress-test \\
 *     --synthesize
 *
 *   # Pick specific models only:
 *   npx tsx scripts/multi-llm-eval.ts \\
 *     --target /the-numbers \\
 *     --type page-audit \\
 *     --models claude-sonnet,gpt-4o,grok-2
 *
 * Output: docs/evaluations/<YYYY-MM-DD>/<target-slug>/<model-key>.md (one per model)
 *         docs/evaluations/<YYYY-MM-DD>/<target-slug>/_synthesis.md (if --synthesize)
 *
 * Required env (one or more depending on --models):
 *   ANTHROPIC_API_KEY, GEMINI_API_KEY (or service account), PERPLEXITY_API_KEY,
 *   OPENAI_API_KEY, GROK_API_KEY (alias: XAI_API_KEY)
 *
 * Honesty gate: this script writes evaluation responses verbatim. Synthesis is
 * generated by Claude or Gemini and labeled as such. None of the output is
 * authoritative — humans read and act on it.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { join, basename, extname } from 'node:path';
import { callAI, MODELS, type Provider } from '../apps/web/lib/ai-models';

// ── Eval type prompt templates ──

const PROMPT_TEMPLATES: Record<string, { system: string; userPrefix: string }> = {
  'page-audit': {
    system:
      'You are a senior brand and product reviewer. Review the page below for accuracy, broken claims, stale references, internal inconsistencies, and risk language (e.g. fundraising claims that imply a regulated offering, fabricated team members, mislabeled photos). Be specific. Quote the offending lines. Suggest fixes. Do not pad your response with marketing talk.',
    userPrefix: 'Audit this page:\n\n',
  },
  'code-review': {
    system:
      'You are a senior software reviewer. Review the code below for correctness, security, performance, and maintainability issues. Flag any anti-patterns. Be specific about line numbers. Suggest concrete fixes.',
    userPrefix: 'Review this code:\n\n```\n',
  },
  'strategy-stress-test': {
    system:
      'You are a skeptical operator who has shipped enterprise SaaS, marketplaces, and creative service businesses. Stress-test the document below. Find structural gaps, optimistic assumptions, missing dependencies, and "this works on paper but not in practice" risks. Be blunt. If something is dumb, say it is dumb and explain why.',
    userPrefix: 'Stress-test this:\n\n',
  },
  'doc-review': {
    system:
      'You are a careful editor reviewing internal documentation. Review the document below for clarity, consistency, completeness, and accuracy. Flag jargon that needs definition. Flag missing context. Suggest sections that should be expanded or cut.',
    userPrefix: 'Review this doc:\n\n',
  },
  'competitive-frame': {
    system:
      'You are a competitive analyst. Given the product description below, describe how a major competitor in the space would position against it, what their weakest counter-arguments would be, and where this product is genuinely defensible vs. easily copied.',
    userPrefix: 'Analyze the competitive position of:\n\n',
  },
};

// ── Default model panel ──

/** Models to query when --models is not specified.
 *  Picks one strong model per provider for diverse perspectives. */
const DEFAULT_PANEL = [
  'claude-sonnet',  // Anthropic
  'gemini-pro',     // Google
  'gpt-4o',         // OpenAI
  'grok-2',         // xAI
  'perplexity',     // Perplexity (web-grounded)
];

// ── Args parsing ──

function parseArgs(): {
  target: string;
  type: string;
  models: string[];
  synthesize: boolean;
  synthesizeWith: string;
} {
  const args = process.argv.slice(2);
  const out = {
    target: '',
    type: '',
    models: DEFAULT_PANEL,
    synthesize: false,
    synthesizeWith: 'claude-sonnet',
  };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--target') out.target = args[++i];
    else if (a === '--type') out.type = args[++i];
    else if (a === '--models') out.models = args[++i].split(',').map((s) => s.trim()).filter(Boolean);
    else if (a === '--synthesize') out.synthesize = true;
    else if (a === '--synthesize-with') out.synthesizeWith = args[++i];
    else if (a === '--help' || a === '-h') {
      printUsage();
      process.exit(0);
    }
  }
  if (!out.target || !out.type) {
    printUsage();
    process.exit(1);
  }
  if (!PROMPT_TEMPLATES[out.type]) {
    console.error(`Unknown --type: ${out.type}. Valid: ${Object.keys(PROMPT_TEMPLATES).join(', ')}`);
    process.exit(1);
  }
  return out;
}

function printUsage() {
  console.error(`
Usage: npx tsx scripts/multi-llm-eval.ts --target <target> --type <eval-type> [--models <comma-list>] [--synthesize]

  --target          A URL (https://...), a file path (docs/foo.md), or a path on the live site (/the-case)
  --type            One of: ${Object.keys(PROMPT_TEMPLATES).join(', ')}
  --models          Comma-separated model keys. Default: ${DEFAULT_PANEL.join(',')}
                    Available keys: ${Object.keys(MODELS).join(', ')}
  --synthesize      After collecting responses, generate a synthesis doc.
  --synthesize-with Model key for synthesis. Default: claude-sonnet.
`);
}

// ── Target loading ──

async function loadTarget(target: string): Promise<{ content: string; sourceLabel: string }> {
  if (target.startsWith('http://') || target.startsWith('https://')) {
    const res = await fetch(target);
    if (!res.ok) throw new Error(`Fetch ${target} → ${res.status}`);
    const text = await res.text();
    return { content: text, sourceLabel: target };
  }
  if (target.startsWith('/')) {
    // Treat as live-site path on the primary domain.
    const url = `https://bigmuddytouring.com${target}`;
    const res = await fetch(url);
    if (!res.ok) throw new Error(`Fetch ${url} → ${res.status}`);
    const text = await res.text();
    return { content: text, sourceLabel: url };
  }
  // File path
  const text = readFileSync(target, 'utf-8');
  return { content: text, sourceLabel: target };
}

// ── Slug helpers ──

function slugify(target: string): string {
  return target
    .replace(/^https?:\/\//, '')
    .replace(/[^a-z0-9]+/gi, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase()
    .slice(0, 80);
}

function todayISO(): string {
  return new Date().toISOString().slice(0, 10);
}

// ── Per-model invocation ──

async function evalOne(modelKey: string, system: string, userMessage: string): Promise<{ ok: true; text: string; label: string } | { ok: false; error: string }> {
  const config = MODELS[modelKey];
  if (!config) return { ok: false, error: `Unknown model key: ${modelKey}` };

  // Find the role that prefers this model. Fallback: 'editorial'.
  // We bypass routing and call the provider directly via callAI's internal CALLERS,
  // but callAI takes a role and does failover. To pin to a specific model, we use
  // a direct fetch call here through a minimal duplicate of the relevant caller.
  // Simpler path: piggyback on the existing role system by using 'editorial' which
  // chains through the providers. For now we directly hit the provider.
  try {
    const text = await callAIDirect(modelKey, config.provider, config.model, system, userMessage);
    return { ok: true, text, label: config.label };
  } catch (e: any) {
    return { ok: false, error: e?.message ?? String(e) };
  }
}

/** Direct provider call without the failover chain — we want to know what THIS
 *  specific model said, not whatever the router picked. */
async function callAIDirect(
  modelKey: string,
  provider: Provider,
  model: string,
  system: string,
  userMessage: string,
): Promise<string> {
  const maxTokens = 4096;
  const temperature = 0.5;

  if (provider === 'anthropic') {
    const key = process.env.ANTHROPIC_API_KEY;
    if (!key) throw new Error('ANTHROPIC_API_KEY not set');
    const res = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: { 'x-api-key': key, 'anthropic-version': '2023-06-01', 'content-type': 'application/json' },
      body: JSON.stringify({
        model,
        max_tokens: maxTokens,
        system,
        messages: [{ role: 'user', content: userMessage }],
      }),
      signal: AbortSignal.timeout(120000),
    });
    if (!res.ok) throw new Error(`Anthropic ${res.status}: ${await res.text()}`);
    const data = await res.json();
    return data.content?.[0]?.text || '';
  }

  if (provider === 'openai') {
    const key = process.env.OPENAI_API_KEY;
    if (!key) throw new Error('OPENAI_API_KEY not set');
    const res = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: { Authorization: `Bearer ${key}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({
        model,
        messages: [{ role: 'system', content: system }, { role: 'user', content: userMessage }],
        max_tokens: maxTokens,
        temperature,
      }),
      signal: AbortSignal.timeout(120000),
    });
    if (!res.ok) throw new Error(`OpenAI ${res.status}: ${await res.text()}`);
    const data = await res.json();
    return data.choices?.[0]?.message?.content || '';
  }

  if (provider === 'grok') {
    const key = process.env.GROK_API_KEY || process.env.XAI_API_KEY;
    if (!key) throw new Error('GROK_API_KEY (or XAI_API_KEY) not set');
    const res = await fetch('https://api.x.ai/v1/chat/completions', {
      method: 'POST',
      headers: { Authorization: `Bearer ${key}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({
        model,
        messages: [{ role: 'system', content: system }, { role: 'user', content: userMessage }],
        max_tokens: maxTokens,
        temperature,
      }),
      signal: AbortSignal.timeout(120000),
    });
    if (!res.ok) throw new Error(`Grok ${res.status}: ${await res.text()}`);
    const data = await res.json();
    return data.choices?.[0]?.message?.content || '';
  }

  if (provider === 'perplexity') {
    const key = process.env.PERPLEXITY_API_KEY;
    if (!key) throw new Error('PERPLEXITY_API_KEY not set');
    const res = await fetch('https://api.perplexity.ai/chat/completions', {
      method: 'POST',
      headers: { Authorization: `Bearer ${key}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({
        model,
        messages: [{ role: 'system', content: system }, { role: 'user', content: userMessage }],
        max_tokens: maxTokens,
        temperature,
      }),
      signal: AbortSignal.timeout(120000),
    });
    if (!res.ok) throw new Error(`Perplexity ${res.status}: ${await res.text()}`);
    const data = await res.json();
    return data.choices?.[0]?.message?.content || '';
  }

  if (provider === 'gemini') {
    // Use the existing callAI path through the router for Gemini — simpler than
    // duplicating Vertex auth here. Pin role=editorial since it includes Gemini in chain.
    const r = await callAI({
      role: 'editorial',
      system,
      messages: [{ role: 'user', content: userMessage }],
      maxTokens,
      temperature,
    } as any);
    return r.text;
  }

  throw new Error(`Unknown provider: ${provider}`);
}

// ── Main ──

async function main() {
  const args = parseArgs();
  console.log(`Multi-LLM evaluation`);
  console.log(`  target: ${args.target}`);
  console.log(`  type: ${args.type}`);
  console.log(`  models: ${args.models.join(', ')}`);
  console.log(`  synthesize: ${args.synthesize ? `yes (with ${args.synthesizeWith})` : 'no'}`);
  console.log('');

  const { content, sourceLabel } = await loadTarget(args.target);
  console.log(`  loaded ${content.length} bytes from ${sourceLabel}`);

  const tpl = PROMPT_TEMPLATES[args.type];
  const userMessage = tpl.userPrefix + content + (args.type === 'code-review' ? '\n```' : '');

  const slug = slugify(args.target);
  const date = todayISO();
  const outDir = join(process.cwd(), 'docs', 'evaluations', date, slug);
  mkdirSync(outDir, { recursive: true });

  console.log(`  output dir: ${outDir}`);
  console.log('');

  // Fan out in parallel.
  console.log('  querying models in parallel...');
  const results = await Promise.all(
    args.models.map(async (key) => ({ key, result: await evalOne(key, tpl.system, userMessage) }))
  );

  const responses: { key: string; label: string; text: string }[] = [];
  for (const { key, result } of results) {
    if (result.ok) {
      const filePath = join(outDir, `${key}.md`);
      const header = `# ${result.label}\n\n**Target:** ${sourceLabel}\n**Eval type:** ${args.type}\n**Date:** ${date}\n\n---\n\n`;
      writeFileSync(filePath, header + result.text, 'utf-8');
      console.log(`  ✓ ${key} (${result.label}) → ${filePath} [${result.text.length} chars]`);
      responses.push({ key, label: result.label, text: result.text });
    } else {
      console.error(`  ✗ ${key}: ${result.error}`);
      const filePath = join(outDir, `${key}.error.md`);
      writeFileSync(filePath, `# ${key} — ERROR\n\n${result.error}\n`, 'utf-8');
    }
  }

  if (args.synthesize && responses.length >= 2) {
    console.log('');
    console.log(`  synthesizing with ${args.synthesizeWith}...`);
    const synthSystem =
      'You are aggregating reviews from multiple LLMs into one synthesis document. Identify (1) where they all agree, (2) where they disagree and what the disagreement is about, (3) what gaps each one caught that the others missed, and (4) a prioritized action list. Be honest about uncertainty. Cite which model said what when it matters.';
    const synthUser =
      `Original target: ${sourceLabel}\nEval type: ${args.type}\n\nResponses:\n\n` +
      responses
        .map((r) => `## ${r.label} (${r.key})\n\n${r.text}\n\n---\n`)
        .join('\n');

    try {
      const synthText = await callAIDirect(
        args.synthesizeWith,
        MODELS[args.synthesizeWith].provider,
        MODELS[args.synthesizeWith].model,
        synthSystem,
        synthUser,
      );
      const synthPath = join(outDir, '_synthesis.md');
      const synthHeader = `# Synthesis (via ${MODELS[args.synthesizeWith].label})\n\n**Target:** ${sourceLabel}\n**Eval type:** ${args.type}\n**Date:** ${date}\n**Source models:** ${responses.map((r) => r.key).join(', ')}\n\n---\n\n`;
      writeFileSync(synthPath, synthHeader + synthText, 'utf-8');
      console.log(`  ✓ synthesis → ${synthPath} [${synthText.length} chars]`);
    } catch (e: any) {
      console.error(`  ✗ synthesis failed: ${e?.message ?? e}`);
    }
  }

  console.log('');
  console.log(`Done. Open the responses in ${outDir} for side-by-side review.`);
}

main().catch((err) => {
  console.error('Eval failed:', err);
  process.exit(1);
});
